public class OperatorEx3 {
    public static void main(String[] args) {
        int i = 5, j = 5;
        System.out.println(i++);
        System.out.println(++j);
        System.out.println("i = " + i + ", j = " + j);

        /*
         * 1번째 출력에서 i 출력 후 1 증가 -> i = 5 출력 후 i = 6
         * 
         * 1번째 출력에서 j를 1 증가 후 출력 -> j = 6 증가 후 출력 j = 6
         */

    }
}
